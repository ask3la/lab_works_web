/**
 * Функция gcd(a, b) возвращает наибольший общий делитель (НОД)
 * двух неотрицательных чисел a и b
 * 
 * Использует алгоритм Евклида для вычисления НОД
 * 
 * @param {number} a - первое неотрицательное число
 * @param {number} b - второе неотрицательное число
 * @returns {number} - наибольший общий делитель чисел a и b
 * @throws {Error} - если a или b не являются неотрицательными числами
 */
function gcd(a, b) {
    // Проверка, что a и b являются неотрицательными числами
    if (!Number.isInteger(a) || a < 0) {
        throw new Error('Параметр a должен быть неотрицательным целым числом');
    }
    
    if (!Number.isInteger(b) || b < 0) {
        throw new Error('Параметр b должен быть неотрицательным целым числом');
    }
    
    // Если одно из чисел равно 0, возвращаем другое число
    // (НОД(0, n) = n, НОД(n, 0) = n)
    if (a === 0) {
        return b;
    }
    
    if (b === 0) {
        return a;
    }
    
    // Алгоритм Евклида:
    // НОД(a, b) = НОД(b, a mod b)
    // Продолжаем, пока b не станет равным 0
    while (b !== 0) {
        // Сохраняем текущее значение b
        let temp = b;
        // b становится остатком от деления a на b
        b = a % b;
        // a становится предыдущим значением b
        a = temp;
    }
    
    // Когда b становится 0, a содержит НОД
    return a;
}

