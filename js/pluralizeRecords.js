/**
 * Функция pluralizeRecords(n) возвращает строку с правильной формой
 * множественного числа для русского языка
 * 
 * Формирует строку вида: "В результате выполнения запроса было найдено n записей"
 * с правильным склонением слов в зависимости от значения n
 * 
 * Правила для русского языка:
 * - one (1, 21, 31, 41, ...): "запись", "было найдено"
 * - few (2-4, 22-24, 32-34, ...): "записи", "были найдены"
 * - many (5-20, 25-30, 35-40, ...): "записей", "были найдены"
 * 
 * @param {number} n - целое неотрицательное число
 * @returns {string} - строка с правильной формой множественного числа
 * @throws {Error} - если n не является целым неотрицательным числом
 */
function pluralizeRecords(n) {
    // Проверка, что n является целым неотрицательным числом
    if (!Number.isInteger(n) || n < 0) {
        throw new Error('Параметр n должен быть целым неотрицательным числом');
    }
    
    // Получаем последнюю цифру и две последние цифры для определения формы
    let lastDigit = n % 10;
    let lastTwoDigits = n % 100;
    
    // Определяем форму слова "запись/записи/записей"
    let recordWord;
    let verbForm; // "было найдено" или "были найдены"
    
    // Если число заканчивается на 1 (но не на 11)
    if (lastDigit === 1 && lastTwoDigits !== 11) {
        recordWord = 'запись';
        verbForm = 'было найдено';
    }
    // Если число заканчивается на 2, 3, 4 (но не на 12, 13, 14)
    else if (lastDigit >= 2 && lastDigit <= 4 && (lastTwoDigits < 12 || lastTwoDigits > 14)) {
        recordWord = 'записи';
        verbForm = 'были найдены';
    }
    // Для всех остальных случаев (0, 5-9, 11, 12-14, 15-20, и т.д.)
    else {
        recordWord = 'записей';
        verbForm = 'были найдены';
    }
    
    // Формируем итоговую строку
    return `В результате выполнения запроса ${verbForm} ${n} ${recordWord}`;
}

