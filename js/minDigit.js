/**
 * Функция minDigit(x) возвращает наименьшую цифру
 * целого неотрицательного числа x
 * 
 * @param {number} x - целое неотрицательное число
 * @returns {number} - наименьшая цифра числа x
 * @throws {Error} - если x не является целым неотрицательным числом
 */
function minDigit(x) {
    // Проверка, что x является целым неотрицательным числом
    if (!Number.isInteger(x) || x < 0) {
        throw new Error('Параметр x должен быть целым неотрицательным числом');
    }
    
    // Если число равно 0, возвращаем 0
    if (x === 0) {
        return 0;
    }
    
    // Инициализируем минимальную цифру максимальным значением (9)
    let min = 9;
    
    // Извлекаем цифры числа, работая с числом напрямую
    // Пока число больше 0
    while (x > 0) {
        // Получаем последнюю цифру через остаток от деления на 10
        let digit = x % 10;
        
        // Сравниваем текущую цифру с минимальной
        // Если текущая цифра меньше минимальной, обновляем минимальную
        if (digit < min) {
            min = digit;
        }
        
        // Удаляем последнюю цифру, используя целочисленное деление
        // (x - x % 10) / 10 эквивалентно Math.floor(x / 10), но без встроенных функций
        x = (x - (x % 10)) / 10;
    }
    
    return min;
}

