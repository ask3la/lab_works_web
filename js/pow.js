/**
 * Функция pow(x, n) возвращает x в степени n
 * Умножает x на себя n раз и возвращает результат
 * Поддерживает только натуральные значения n (положительные целые числа)
 * 
 * @param {number} x - основание степени
 * @param {number} n - показатель степени (натуральное число)
 * @returns {number} - результат возведения x в степень n
 * @throws {Error} - если n не является натуральным числом
 */
function pow(x, n) {
    // Проверка, что n является натуральным числом
    // Натуральное число - это положительное целое число (1, 2, 3, ...)
    if (!Number.isInteger(n) || n <= 0) {
        throw new Error('Показатель степени n должен быть натуральным числом (положительным целым числом)');
    }
    
    // Если степень равна 1, возвращаем само число
    if (n === 1) {
        return x;
    }
    
    // Инициализируем результат единицей для умножения
    let result = 1;
    
    // Умножаем x на себя n раз
    for (let i = 0; i < n; i++) {
        result *= x;
    }
    
    return result;
}

